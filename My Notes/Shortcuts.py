"""
Önceden bildiğim kısa yollar. 
"""
# Copy (Kopyala): CTRL + C
# Paste (Yapıştır): CTRL + V
# Cut (Kes): CTRL + X

# Undo (Geri al): CTRL + Z
# Redo (Geri aldığın şeyi tekrar geri al): CTRL + Y

# Add Line Comment (Toplu şekilde yorum satırı oluşturmak): CTRL + K + C 
# Remove Line Comment (Toplu şekilde yorum satırını kaldırmak): CTRL + K + U


"""
Önemli bulduğum ve yeni öğrendiğim kısa yollar. :D
"""
# Indent Line (Girinti oluşturmak): CTRL + Ü

# Move Line Up (Satırı yukarıya taşımak): ALT + UpArrow
# Move Line Down (Satırı aşağıya taşımak): ALT + DownArrow

# Highlighting Characters (Karakterleri vurgulamak): SHIFT + RightArrow
# Removing Highlighted Characters (Vurgulanan karakterleri kaldırmak): SHIFT + LeftArrow
# Highlight Line By Line (Satır satır vurgulamak): CTRL + L
# Kelime kelime vurgula => SHIFT + CTRL + RightArrow or LeftArrow

# Move Word By Word (Kelime kelime ilerlemek): CTRL + RightArrow
# Move Word By Word But In Reverse (Kelime kelime ilerlemek ama geriye doğru): CTRL + LeftArrow

# Select Word and Replace (Kelime seç ve değiştir): CTLR + D (Birden fazla kez bu 
# kombinasyonu kullanarak aynı kelimeyi diğer kod satırlarından da seçerek bir yerdeki 
# değişiklik ile aynı anda hepsini değiştirebilirsin.)
# Sağdan sola kelime kelime sil => CTRL + DELETE

# Set Multiple Cursors (Birden çok imleç ayarlamak): ALT + CLICK (Birden fazla kez bu 
# kombinasyonu kullanarak seçtiğin yerlere ilk tıkladığın yere yazdığın bilgiyi aynı anda
# hepsine yazmaya yarar.)

# Run Code (Kodu Çalıştır): CTRL + ALT + N 
# Close / Open Explorer Window (Dosya Penceresini Aç / Kapa): CTRL + B 

# One Time Capital Letter (Bir Kerelik Büyük Harf): SHIFT + Press Any Letter

# Select and Replace All Words (Bir Kelimeyi Değiştirmek İstiyorsan Tek Bir Yerdeki O 
# Kelimeye Tıkladıktan Sonra Her Yerdeki O Kelimeyi Değiştirebilirsin): CTRL + SHIFT + L
# Select and Replace Desired Words (Bir Kelimeyi Değiştirmek İstiyorsan Tek Bir Yerdeki 
# O Kelimeye Tıkladıktan Sonra Bu Kombinasyonu Tekrar Tekrar Kullanarak Diğer Yerlerden 
# İstediklerini Seçip Değiştirebilirsin): CTRL + D
